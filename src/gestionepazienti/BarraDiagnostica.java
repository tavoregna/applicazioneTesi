    public void generaEtichette(int numeroEtichette)
    {
        double altezza=this.getSize().getHeight();
        double larghezza=this.getSize().getWidth();
        for(int i=0;i<numeroEtichette;i++)
        {
            double alt=altezza/numeroEtichette;
            JLabel eti=new JLabel("Pulcina");
            eti.setBounds(0, (int)(i*alt), (int)larghezza, (int)(alt));
            eti.setHorizontalAlignment(SwingConstants.CENTER);
            eti.setVerticalAlignment(SwingConstants.CENTER);
            this.add(eti);
        }
    }

